package com.gpcoder;

public class ForSample1 {
	public static void main(String[] args) {

		System.out.print("Vòng lặp for với biến tăng: ");
		for (int i = 1; i <= 5; i++) {
			System.out.print(i + " ");
		}

		// Hiển thị ký tự xuống dòng
		System.out.println("");
		System.out.print("Vòng lặp for với biến giảm: ");
		for (int i = 5; i >= 1; i--) {
			System.out.print(i + " ");
		}

	}
}